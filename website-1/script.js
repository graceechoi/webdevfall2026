const players = document.querySelectorAll(".music-player-small, .music-player-full");

players.forEach(player => {

    const era = player.dataset.era;
    const songs = playlists[era];

    let currentIndex = 0;

    const songElement = player.querySelector(".song");
    const artistElement = player.querySelector(".artist");
    const nextButton = player.querySelector(".next");
    const prevButton = player.querySelector(".prev");
    const playButton = player.querySelector(".play");
    const imageElement = player.querySelector("img");

    function updatePlayer() {
        const currentSong = songs[currentIndex];

        songElement.textContent = currentSong.title;
        songElement.href = currentSong.songLink;
        playButton.href = currentSong.songLink;

        artistElement.textContent = currentSong.artist;
        artistElement.href = currentSong.artistLink;

        imageElement.src = currentSong.image;
    }

    nextButton.addEventListener("click", () => {
        currentIndex = (currentIndex + 1) % songs.length;
        updatePlayer();
    });

    prevButton.addEventListener("click", () => {
        currentIndex = (currentIndex - 1 + songs.length) % songs.length;
        updatePlayer();
    });

    updatePlayer();
});